    <h2>Trafficfeed Getting Started</h2>
    <h3>How to use this plugin?</h3>
    <ol>
        <li>
           Register for a free trafficfeed.com account from within plugin. Your site will be added automatically once you register.
        </li>
        <li>
           Then you need to go to your wordpress installation(Appearance -> Widgets) and add Trafficfeed Widget to  the place where you would like links to appear. (Sidebar or Footer recommended ). And if you are familiar with the system you can add the proper code anywhere you would like links to appear.
        </li>
        <li>
            Then you need to login into your trafficfeed account and add pages that you want to use for link window or link directory.
        </li>
        <li>
            Start using the system, GO TO “Exchange by Catalogue” and find link partners.
        </li>
        <li>
            GO TO “Auto Exchange Settings” add sites and pages you would like to participate. (auto exchange is enabled by default)
        </li>
         <li>
            For more details please visit <a href="http://www.trafficfeed.com?ref=wp_getting_started" target="_blank" title="trafficfeed.com">TrafficFeed.com</a>
        </li>
    </ol>  
